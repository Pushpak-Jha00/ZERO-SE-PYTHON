# <class 'float'>
a = 31.22
t = type(a)
print(t);

# <class 'int'>
a = 31
t = type(a)
print(t);

# <class 'str'>
a = "31.22"
t = type(a)
print(t);

# <class 'bool'>
a = True
t = type(a)
print(t);


